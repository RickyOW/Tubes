package com.ricky.com.antrian;

public class JenisSalon {
    private String nama;
    int gambar;

    public JenisSalon(String nama, int gambar){
        this.nama = nama;
        this.gambar = gambar;
    }
    public String getNama() {
        return nama;
    }

    public int getGambar() {
        return gambar;
    }



}
